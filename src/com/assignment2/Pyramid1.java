package com.assignment2;

public class Pyramid1 
{
	public static void main(String[] args) 
	{
		 
		int n=65;
		for(int i=5;i>0;i--)
		{
		String s="";
		for(int j=0;j<i;j++)
		{
		n++; 
		s+=n+" ";
		}
		System.out.println(s);
		}
	    		   System.out.println();
	    	   }
	    	  
			
		
	}


